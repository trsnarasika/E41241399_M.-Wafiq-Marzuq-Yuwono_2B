private void searchData(String keyword) {
    DefaultTableModel model = (DefaultTableModel) jtabeltransaksibeli.getModel();
    model.setRowCount(0);

    String sql = "SELECT * FROM transaksi_beli WHERE id_transaksi_beli LIKE ? OR nama_barang LIKE ?";
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {

        stmt.setString(1, "%" + keyword + "%");
        stmt.setString(2, "%" + keyword + "%");

        try (ResultSet rs = stmt.executeQuery()) {
            while (rs.next()) {
               
                String id_transaksi = rs.getString("id_transaksi_beli");
                String id_suplier = rs.getString("id_suplier");
                String nama_barang = rs.getString("nama_barang");
                String jumlah = rs.getString("jumlah");
                String tanggal = rs.getString("tanggal");
                String harga = rs.getString("total_harga");
                String id_barang = rs.getString("id_barang");
                String username = rs.getString("username");
               
                Object[] rowData = {
                   id_transaksi,id_suplier,nama_barang,jumlah,tanggal,harga,id_barang,username
                };
                model.addRow(rowData);
            }
        }
    } catch (Exception e) {
        Logger.getLogger(Fitur_TransaksiBeli.class.getName()).log(Level.SEVERE, "Error saat mencari data", e);
        JOptionPane.showMessageDialog(this, 
            "Error saat mencari data: " + e.getMessage(), 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jcariActionPerformed

