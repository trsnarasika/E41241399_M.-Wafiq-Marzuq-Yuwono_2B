    private void getData() {
    DefaultTableModel model = (DefaultTableModel) jtabeltransaksibeli.getModel();
    model.setRowCount(0); 
    String sql = "SELECT * FROM Transaksi_beli";

   
    try (PreparedStatement st = conn.prepareStatement(sql);
         ResultSet rs = st.executeQuery()) {
        
       
        while (rs.next()) {
            String idtransaksi = rs.getString("id_transaksi_beli");
            String idsuplier = rs.getString("id_suplier");
            String namabarang = rs.getString("Nama_barang");
            String jumlah = rs.getString("jumlah");
            String tanggal = rs.getString("tanggal");
            
           
            BigDecimal totalharga = rs.getBigDecimal("total_harga");
            String idbarang = rs.getString("id_barang");
            String username = rs.getString("username");

            
            Object[] rowData = {idtransaksi, idsuplier, namabarang, jumlah, tanggal, totalharga, idbarang, username};
            model.addRow(rowData);
        }
    } catch (Exception e) {
        
        Logger.getLogger(Fitur_TransaksiBeli.class.getName()).log(Level.SEVERE, null, e);
        JOptionPane.showMessageDialog(this, "Error saat menambahkan data: " + e.getMessage());
    }
}

 

