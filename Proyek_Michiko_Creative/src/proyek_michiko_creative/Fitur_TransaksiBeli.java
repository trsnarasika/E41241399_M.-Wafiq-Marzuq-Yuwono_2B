    private void jubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jubahActionPerformed
   
    int selectedRow = jtabeltransaksibeli.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih tabel yang akan diubah");
        return;
    }

    String idtransaksi = jtabeltransaksibeli.getValueAt(selectedRow, 0).toString();

    String idsuplier = tidsuplier.getText();
    String namabarang = tnamabarang.getText();
    String jumlah = tjumlah.getText();
    String tanggal = ttanggal.getText();
    String totalharga = ttotalharga.getText();
    String idbarang = tidbarang.getText();
    String penjual = tusername.getText();

    if (idsuplier.isEmpty() || namabarang.isEmpty() || jumlah.isEmpty() || tanggal.isEmpty() || totalharga.isEmpty() || idbarang.isEmpty() || penjual.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Harap lengkapi semua field sebelum mengubah data.");
        return;
    }

    int jumlaha = 0;
    BigDecimal totalhargaa = BigDecimal.ZERO; 
    try {
        jumlaha = Integer.parseInt(jumlah);
        totalhargaa = new BigDecimal(totalharga); 
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Jumlah dan Total Harga harus berupa angka yang valid.");
        return;
    }

 
    String sql = "UPDATE transaksi_beli SET jumlah = ?, total_harga = ? WHERE id_transaksi_beli = ?";

    try (PreparedStatement st = conn.prepareStatement(sql)) {
        st.setInt(1, jumlaha);           
        st.setBigDecimal(2, totalhargaa); 
        st.setString(3, idtransaksi);   

       
        int rowsUpdated = st.executeUpdate();
        if (rowsUpdated > 0) {
            JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");
            getData();  
            resetForm();
        } else {
            JOptionPane.showMessageDialog(this, "Data dengan ID tersebut tidak ditemukan atau gagal diperbarui.");
        }
    } catch (SQLException e) {
        Logger.getLogger(Fitur_TransaksiBeli.class.getName()).log(Level.SEVERE, null, e);
        JOptionPane.showMessageDialog(this, "Error saat mengubah data: " + e.getMessage());
    }

    }//GEN-LAST:event_jubahActionPerformed

    private void jhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jhapusActionPerformed
        // TODO add your handling code here:
        
int selectedRow= jtabeltransaksibeli.getSelectedRow();
            if(selectedRow == 1){
                JOptionPane.showMessageDialog(this,"Pilih tabel yang akan di ubah");
                return;
            }
            String id_barang = jtabeltransaksibeli.getValueAt(selectedRow, 0).toString(); 
    int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus item ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            
            String sql = "DELETE FROM transaksi_beli WHERE id_transaksi_beli = ?";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, id_barang);
            int rowsDeleted = st.executeUpdate();
            
            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                getData();
                resetForm(); 
                
            }
            
            st.close();
        } catch (Exception e) {
            Logger.getLogger(Fitur_TransaksiBeli.class.getName()).log(Level.SEVERE, null, e);
            JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jhapusActionPerformed

