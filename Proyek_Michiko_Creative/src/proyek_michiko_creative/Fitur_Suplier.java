    private void jhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jhapusActionPerformed
        // TODO add your handling code here:
    int selectedRow = jtabell.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih tabel yang akan dihapus");
        return;
    }

    String id_suplier = jtabell.getValueAt(selectedRow, 0).toString();
    
    int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus item ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            String sql = "DELETE FROM suplier WHERE id_suplier = ?";
            PreparedStatement st = conn.prepareStatement(sql);
            st.setString(1, id_suplier);
            int rowsDeleted = st.executeUpdate();
            
            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
                getData(); // Memperbarui data yang ada di tabel
                resetForm(); // Mengosongkan form input
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan atau gagal dihapus.");
            }
            
            st.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage());
        }
    }

    }//GEN-LAST:event_jhapusActionPerformed

