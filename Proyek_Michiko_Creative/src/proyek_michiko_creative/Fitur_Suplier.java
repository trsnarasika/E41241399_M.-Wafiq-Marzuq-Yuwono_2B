     private void getData() {
    DefaultTableModel model = (DefaultTableModel) jtabell.getModel();
    model.setRowCount(0); 

    try { 
        String sql = "SELECT * FROM suplier"; 
        PreparedStatement st = conn.prepareStatement(sql); 
        ResultSet rs = st.executeQuery();

        while (rs.next()) { 
            String id_suplier = rs.getString("id_suplier");  
            String nama_suplier = rs.getString("Nama_Suplier");
            String no_telp = rs.getString("no_telp");
            String alamat = rs.getString("alamat_suplier");

            
            Object[] rowData = {id_suplier, nama_suplier, no_telp, alamat}; 
            model.addRow(rowData);
        }

        
        rs.close(); 
        st.close();
    } 
    catch (Exception e) { 
        Logger.getLogger(Fitur_Suplier.class.getName()).log(Level.SEVERE, null, e); 
        JOptionPane.showMessageDialog(this, "Error saat menambahkan data: " + e.getMessage());
    }
}




