private void searchData(String keyword) {
    DefaultTableModel model = (DefaultTableModel) jtabell.getModel();
    model.setRowCount(0); 

    String sql = "SELECT * FROM suplier WHERE id_suplier LIKE ? OR Nama_Suplier LIKE ?";
    try (PreparedStatement stmt = conn.prepareStatement(sql)) {
        stmt.setString(1, "%" + keyword + "%");
        stmt.setString(2, "%" + keyword + "%");

        try (ResultSet rs = stmt.executeQuery()) {
            while (rs.next()) {
                String id_suplier = rs.getString("id_suplier");
                String nama_suplier = rs.getString("Nama_Suplier");
                String alamat = rs.getString("alamat_suplier");
                String notlp = rs.getString("no_telp");
                String username = rs.getString("username");
               
                Object[] rowData = {
                   id_suplier,nama_suplier,alamat,notlp
                };
                model.addRow(rowData);
            }
        }
    } catch (Exception e) {
        Logger.getLogger(Fitur_Suplier.class.getName()).log(Level.SEVERE, "Error saat mencari data", e);
        JOptionPane.showMessageDialog(this, 
            "Error saat mencari data: " + e.getMessage(), 
            "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jcariActionPerformed

